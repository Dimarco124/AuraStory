// Ajouter un produit au panier
function ajouterAuPanier(idProduit) {
    const produit = produits.find(p => p.id === idProduit);
    if (!produit) return;

    const articleExistant = panier.find(item => item.id === idProduit);
    if (articleExistant) {
        articleExistant.quantite += 1;
    } else {
        panier.push({
            id: produit.id,
            titre: produit.titre,
            prix: produit.prixActuel,
            image: produit.image,
            quantite: 1
        });
    }

    mettreAJourCompteurPanier();
    afficherArticlesPanier();
    afficherNotificationPanier();
}

// Mettre à jour le compteur du panier dans l'en-tête
function mettreAJourCompteurPanier() {
    const compteurPanier = document.querySelector('.compteur-panier');
    const totalArticles = panier.reduce((total, item) => total + item.quantite, 0);
    compteurPanier.textContent = totalArticles;
}

// Afficher les articles du panier
function afficherArticlesPanier() {
    articlesPanier.innerHTML = '';
    
    if (panier.length === 0) {
        articlesPanier.innerHTML = '<p>Votre panier est vide</p>';
        totalPanier.textContent = '0,00FCFA';
        return;
    }

    let total = 0;
    
    panier.forEach(item => {
        const valeurPrix = parseFloat(item.prix.replace('FCFA', '').replace(',', '.'));
        const totalArticle = valeurPrix * item.quantite;
        total += totalArticle;
        
        const articlePanier = document.createElement('div');
        articlePanier.className = 'article-panier';
        articlePanier.innerHTML = `
            <img src="${item.image}" alt="${item.titre}" class="image-article-panier">
            <div class="details-article-panier">
                <h4 class="titre-article-panier">${item.titre}</h4>
                <div class="prix-article-panier">${item.prix} x ${item.quantite}</div>
                <button class="supprimer-article" data-id="${item.id}"><i class="fa fa-trash"></i></button>
            </div>
        `;
        articlesPanier.appendChild(articlePanier);
    });

    totalPanier.textContent = total.toFixed(2).replace('.', ',') + 'FCFA';
    
    // Ajouter les écouteurs d'événements aux boutons de suppression
    document.querySelectorAll('.supprimer-article').forEach(bouton => {
        bouton.addEventListener('click', function() {
            const idProduit = parseInt(this.getAttribute('data-id'));
            supprimerDuPanier(idProduit);
        });
    });
}

// Supprimer un article du panier
function supprimerDuPanier(idProduit) {
    panier = panier.filter(item => item.id !== idProduit);
    mettreAJourCompteurPanier();
    afficherArticlesPanier();
}

