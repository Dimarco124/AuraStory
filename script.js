// Fonctionnalité du carrousel
function configurerCarrousel() {
    let diapositiveActuelle = 0;
    const diapositives = document.querySelectorAll('.element-carrousel');
    const totalDiapositives = diapositives.length;
    
    // Définir la position initiale
    mettreAJourCarrousel();
    
    // Ajouter les événements de clic aux points
    pointsCarrousel.forEach(point => {
        point.addEventListener('click', function() {
            diapositiveActuelle = parseInt(this.getAttribute('data-index'));
            mettreAJourCarrousel();
        });
    });
    
    // Défilement automatique toutes les 5 secondes
    setInterval(() => {
        diapositiveActuelle = (diapositiveActuelle + 1) % totalDiapositives;
        mettreAJourCarrousel();
    }, 5000);
    
    function mettreAJourCarrousel() {
        conteneurCarrousel.style.transform = `translateX(-${diapositiveActuelle * 100}%)`;
        
        // Mettre à jour le point actif
        pointsCarrousel.forEach((point, index) => {
            if (index === diapositiveActuelle) {
                point.classList.add('actif');
            } else {
                point.classList.remove('actif');
            }
        });
    }
}


